import os
import random

def set_firewalld():
    print("\033[1;33m正在关闭防火墙...\033[0m")
    # 这里可以加入真正的防火墙关闭命令，例如：
    # os.system("systemctl stop firewalld")
    input("按 Enter 键返回菜单...")  # 等待用户按回车

def set_all():
    while True:
        os.system("clear")  # 清屏
        color = random.randint(31, 37)
        print(f"\033[{color};1m")  # 随机颜色

        print("""
************************************************************
************************************************************
************************************************************
**********                                        ********** 
**********              系统设置                  ********** 
**********                                        **********
**********  2.关闭防火墙                           **********
************************************************************       
************************************************************
************************************************************
        """)
        print("\033[0m")  # 恢复默认颜色

        menu = input("\033[1;36m请输入序号: \033[0m")

        if menu == "2":
            set_firewalld()  # 调用防火墙设置函数
        else:
            print("\n\033[1;31m输入无效! 请输入正确的选项!!!!!!\033[0m")
            input("按 Enter 键继续...")  # 防止错误提示一闪而过

def main():
    while True:
        os.system("clear")  # 清屏
        color = random.randint(31, 37)
        print(f"\033[{color};1m")  # 随机颜色

        print("""
************************************************************
************************************************************
************************************************************
**********                                        **********
**********            Linux运维脚本               **********
**********                                        **********
**********            1.系统设置                  **********
************************************************************
************************************************************
************************************************************
        """)
        print("\033[0m")  # 恢复默认颜色

        option = input("\033[1;34m输入选项: \033[0m")

        if option == "1":
            set_all()  # 进入 "系统设置" 菜单
        else:
            print("\n\033[1;31m输入无效! 请输入正确的选项!!!\033[0m")
            input("按 Enter 键继续...")  # 防止错误提示一闪而过

if __name__ == "__main__":
    main()
